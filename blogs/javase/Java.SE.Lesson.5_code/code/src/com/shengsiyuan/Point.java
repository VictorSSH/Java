package com.shengsiyuan;

public class Point
{
	int x;
	int y; 
	 
	/**
	 * 
	 * @param x coordinate of x
	 * @param y coordinate of y
	 */
	public Point(int x, int y)
	{
		this.x = x;
		this.y = y;
	}
	
	/**
	 * @return return coordinate of axis of x and y
	 */
	
	public String toString()
	{
		return "x=" + this.x + ", y=" + this.y;
	}
	
	
	
	
	
	
	
	
	
	
	
	
}
